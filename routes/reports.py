@router.get(
    "/export/excel",
    summary="导出报表为 Excel"
)
async def export_report_excel(
    report_type: str = Query(..., description="报表类型（summary/booths/products/transactions）"),
    event_id: Optional[int] = Query(None, description="活动ID（可选）"),
    db: Session = Depends(get_db),
    current_user = Depends(require_role(["super_admin", "event_admin", "reviewer"]))
):
    """
    导出报表为 Excel 文件。
    
    支持的报表类型：
    - summary: 总览统计
    - booths: 摊位报表
    - products: 商品报表
    - transactions: 交易流水
    
    权限要求：super_admin, event_admin, reviewer
    """
    try:
        export_service = ExportService(db)
        excel_data = export_service.export_to_excel(report_type, event_id)
        
        logger.info(
            f"Report exported by user {current_user.username} "
            f"for report_type={report_type}, event_id={event_id}"
        )
        
        # 生成文件名
        filename = f"report_{report_type}"
        if event_id:
            filename += f"_event{event_id}"
        filename += ".xlsx"
        
        # 返回 Excel 文件
        return StreamingResponse(
            io.BytesIO(excel_data),
            media_type="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
            headers={
                "Content-Disposition": f"attachment; filename={filename}"
            }
        )
    
    except ValueError as e:
        logger.warning(f"Invalid export request: {str(e)}")
        raise HTTPException(status_code=400, detail=str(e))
    
    except Exception as e:
        logger.error(f"Error exporting report: {str(e)}", exc_info=True)
        raise HTTPException(status_code=500, detail="Failed to export report")
