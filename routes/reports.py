@router.get(
    "/audit-logs",
    response_model=AuditLogResponse,
    summary="获取异常审计日志"
)
async def get_audit_logs(
    event_id: Optional[int] = Query(None, description="活动ID（可选）"),
    flag_type: str = Query(
        "all",
        description="标记类型（all/high_frequency_refund/large_adjustment/suspicious_operation）"
    ),
    limit: int = Query(100, description="返回数量限制", ge=1, le=1000),
    db: Session = Depends(get_db),
    current_user = Depends(require_role(["super_admin", "event_admin", "reviewer"]))
):
    """
    获取异常审计日志。
    
    标记规则：
    - high_frequency_refund: 高频退款（同一操作员在1小时内退款超过5次）
    - large_adjustment: 大额更正（adjust 类型且金额 > 1000元）
    - suspicious_operation: 可疑操作（深夜交易：22:00-06:00）
    
    权限要求：super_admin, event_admin, reviewer
    """
    try:
        report_service = ReportService(db)
        audit_logs = report_service.get_audit_logs(event_id, flag_type, limit)
        
        logger.info(
            f"Audit logs retrieved by user {current_user.username} "
            f"for event_id={event_id}, flag_type={flag_type}"
        )
        
        return audit_logs
    
    except Exception as e:
        logger.error(f"Error retrieving audit logs: {str(e)}", exc_info=True)
        raise HTTPException(status_code=500, detail="Failed to retrieve audit logs")


