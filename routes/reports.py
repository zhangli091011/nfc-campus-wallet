logger = logging.getLogger(__name__)

router = APIRouter(prefix="/reports")


# ============================================================================
# Summary Report
# ============================================================================

@router.get(
    "/summary",
    response_model=SummaryReportResponse,
    summary="获取总览统计报表"
)
async def get_summary_report(
    event_id: Optional[int] = Query(None, description="活动ID（可选，不填则统计所有活动）"),
    db: Session = Depends(get_db),
    current_user = Depends(require_role(["super_admin", "event_admin", "reviewer"]))
):
    """
    获取总览统计报表。
    
    统计指标：
    - 总发放额度
    - 总充值额
    - 总消费额
    - 总退款额
    - 净消费额
    - 总交易笔数
    - 参与者数量
    - 摊位数量
    
    权限要求：super_admin, event_admin, reviewer
    """
    try:
        report_service = ReportService(db)
        summary = report_service.get_summary_report(event_id)
        
        logger.info(
            f"Summary report generated by user {current_user.username} "
            f"for event_id={event_id}"
        )
        
        return summary
    
    except Exception as e:
        logger.error(f"Error generating summary report: {str(e)}", exc_info=True)
        raise HTTPException(status_code=500, detail="Failed to generate summary report")


